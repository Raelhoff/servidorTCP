package model;

/**
 *
 * @author RaelH
 */
public class Registration {
    private int id;
    private String mensagem;
    private String data;
    
    public Registration() {
    }
    
    public Registration(String mensagem, String data) {
        this.mensagem = mensagem;
        this.data = data;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getMensagem() {
        return mensagem;
    }

    public void setMensagem(String mensagem) {
        this.mensagem = mensagem;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }
    
    
}
